public class PartA {
    public static void main(String[] args)
    {
        for (int cnt = 0; cnt <= 30; cnt++) // task 1
        {
            System.out.println(cnt);
        }

        System.out.println("\n\n\n\n\n"); // separation for tasks

        for (int cnt=30; cnt>=0; cnt--) // task 2
        {
            System.out.println(cnt);
        }

        System.out.println("\n\n\n\n\n"); // separation for tasks

        for (int cnt=0; cnt<=18; cnt = cnt + 3) // task 3
        {
            System.out.println(cnt);
        }

        System.out.println("\n\n\n\n\n"); // separation for tasks

        for (int cnt=10; cnt>=0; cnt = cnt - 2) // task 4
        {
            System.out.println(cnt);
        }
    }
}